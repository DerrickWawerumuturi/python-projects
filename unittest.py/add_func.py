def add(a, b):
    """
    Returns a + b
    """
    return a + b

def sub(a, b):
    """ Returns a - b"""
    return a - b
    
def mul(a, b):
    """ returns a * b """
    return a * b

def div(a, b):
    """ return  a  /  b """
    return a / b    
    
def mod(a, b):
    """ returns  a % b """
    return a % b